#Displaying the ASCII value of a character


print("***ASCII and Character conversion**")

#Conversion from character to ASCII
character=input("Enter character: ")
print("The ASCII value is:",ord(character))

#Conversion from ASCII to character
Ascii=int(input("Enter the ASCII value: "))
print("The character is:",chr(Ascii))